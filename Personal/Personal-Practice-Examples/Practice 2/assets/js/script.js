
// Write a function that takes in an array of numbers and outputs the maximum number. For example...
// Input: [ 1, 2, 3 ]
// Output: 3
function maxOutputFirst () {
   console.log(Math.max (1, 3, 2));
}

maxOutputFirst();


// Input: [ 3, 6, 4, 5, 2, 1 ]
// Output: 6
function maxOutputSecond () {
   console.log(Math.max (3, 6, 4, 5, 2, 1));
}

maxOutputSecond();

// Input: [ 3, 3, 3 ]
// Output: 3
function maxOutputThird () {
   console.log(Math.max (3, 3, 3));
}

maxOutputThird();